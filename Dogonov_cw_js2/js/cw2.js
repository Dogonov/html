var num = +prompt("Введите число");
switch (num) {
    case 5:
        alert("Tiny");
        break;
    case 10:
        alert("Small");
        break;
    case 15:
        alert("Medium");
        break;
    case 20:
        alert("Large");
        break;
    case 25:
        alert("Huge");
        break;
    default:
        alert("Данного число неверно.");
}
